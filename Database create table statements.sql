DROP TABLE IF EXISTS `TEAM_INFO`;
CREATE TABLE IF NOT EXISTS `TEAM_INFO` (
   `TEAM_ID` int(11) NOT NULL AUTO_INCREMENT,
   `TEAM_NAME` varchar(40) NOT NULL,
   `LEAGUE_ID` int(11) NOT NULL,
   `UPDATE_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
   PRIMARY KEY (`TEAM_ID`)
);

DROP TABLE IF EXISTS `USER_TEAM_INFO`;
CREATE TABLE IF NOT EXISTS `USER_TEAM_INFO` (
   `TEAM_ID` int(11) NOT NULL,
   `USER_ID` int(11) NOT NULL,
   `START_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
   `END_DTM` timestamp,
   `UPDATE_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
   PRIMARY KEY (`TEAM_ID`,`USER_ID`)
);


DROP TABLE IF EXISTS `PLAYER_INFO`;
CREATE TABLE IF NOT EXISTS `PLAYER_INFO` (
  `PLAYER_ID` int(11) NOT NULL,
  `FIRST_NAME` varchar(40) NOT NULL,
  `LAST_NAME` varchar(40) NOT NULL,
  `BAT_BOWL_STYLE` varchar(100) NOT NULL,
  `CATEGORY` varchar(100) NOT NULL,
  `BASE_PRICE` int(11) NOT NULL,
  `RUNSFACTOR` int(11) NOT NULL DEFAULT '1',
  `IPL_TEAM_ID` int(11) NOT NULL,
  `UPDATE_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`PLAYER_ID`)
);
DROP TABLE IF EXISTS `AUCTION_MESSAGES_INFO`;
CREATE TABLE IF NOT EXISTS `AUCTION_MESSAGES_INFO` (
   `MESSAGE_ID` int(11) NOT NULL AUTO_INCREMENT,
   `USER_NAME` varchar(40) NOT NULL,
   `USER_ID` int(11) NOT NULL DEFAULT 0,
   `MESSAGE` varchar(40) NOT NULL,
   `UPDATE_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
   PRIMARY KEY (`MESSAGE_ID`)
);

DROP TABLE IF EXISTS `AUCTION_PLAYER_INFO`;
CREATE TABLE IF NOT EXISTS `AUCTION_PLAYER_INFO` (
   `AUCTION_ID` int(11) DEFAULT 1,
