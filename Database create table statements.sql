DROP TABLE IF EXISTS `USER_INFO`;
CREATE TABLE IF NOT EXISTS `USER_INFO` (
   `USER_ID` int(11) NOT NULL AUTO_INCREMENT,
   `USER_FIRST_NAME` varchar(40) NOT NULL,
   `USER_LAST_NAME` varchar(40) NOT NULL,
   `IS_ADMIN` BOOLEAN NOT NULL DEFAULT FALSE,
   `EMAIL_ID` varchar(40) NOT NULL DEFAULT '',
   `PASSWORD` varchar(100) NOT NULL,
   `UPDATE_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
   PRIMARY KEY (`USER_ID`)
);

DROP TABLE IF EXISTS `LEAGUE_INFO`;
CREATE TABLE IF NOT EXISTS `LEAGUE_INFO` (
   `LEAGUE_ID` int(11) NOT NULL AUTO_INCREMENT,
   `LEAGUE_NAME` varchar(40) NOT NULL,
   `ADMIN_ID` int(11) NOT NULL,
   `IS_ACTIVE` BOOLEAN NOT NULL DEFAULT TRUE,
   `UPDATE_DTM` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
   PRIMARY KEY (`LEAGUE_ID`)
);
